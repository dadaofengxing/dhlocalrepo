package com.dh.ddfx.lz.module;


/**
 * @author DELL
 */
public class Product {
    private String id;
    private String src;
    private String length;
    private String renLength;
    private String bingLength;
    private String weight;
    private int index;
    private int status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public String getRenLength() {
        return renLength;
    }

    public void setRenLength(String renLength) {
        this.renLength = renLength;
    }

    public String getBingLength() {
        return bingLength;
    }

    public void setBingLength(String bingLength) {
        this.bingLength = bingLength;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
